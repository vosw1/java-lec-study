package ex04;

// 계산기 클래스 = 설계도
public class Calculator {
    public double add(double n1, double n2){
        return n1 + n2;
    }

    public double minus(double n1, double n2){
        return n1 - n2;
    }

    public double divid(double n1, double n2){
        return n1 / n2;
    }

    public double multi(double n1, double n2){
        return n1 * n2;
    }

}
